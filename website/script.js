const languageSelect = document.getElementById("language");
const languageLabel = document.querySelector("[data-language-label]");
const fallbackOrderHref = "mailto:hello@lazying.art?subject=Patchwork%20Leather%20Notebook";
let smartTextFrame = 0;

function normalizeLanguage(language) {
  const value = String(language || "").toLowerCase();
  if (["zh-hant", "zh-tw", "zh-hk", "zh-mo"].includes(value)) {
    return "zh-Hant";
  }
  if (["zh", "zh-cn", "zh-hans", "zh-sg"].includes(value)) {
    return "zh-Hans";
  }
  if (value.startsWith("ja")) {
    return "ja";
  }
  if (value.startsWith("ko")) {
    return "ko";
  }
  if (value.startsWith("vi")) {
    return "vi";
  }
  if (value.startsWith("ar")) {
    return "ar";
  }
  if (value.startsWith("fr")) {
    return "fr";
  }
  if (value.startsWith("es")) {
    return "es";
  }
  if (value.startsWith("de")) {
    return "de";
  }
  if (value.startsWith("ru")) {
    return "ru";
  }
  return translations[language] ? language : "en";
}

function preferredBrowserLanguage() {
  return normalizeLanguage(navigator.language || "en");
}

function renderLocalizedText(node, value, language) {
  node.style.fontSize = "";
  node.textContent = "";

  if (!Array.isArray(value)) {
    node.textContent = value;
    return;
  }

  const separator = ["zh-Hans", "zh-Hant", "ja"].includes(language) ? "\u200b" : " ";
  if (!node.classList.contains("smart-text")) {
    node.textContent = value.join(" ");
    return;
  }

  value.forEach((chunk, index) => {
    const span = document.createElement("span");
    span.className = "text-chunk";
    span.textContent = chunk;
    node.append(span);

    if (separator && index < value.length - 1) {
      node.append(document.createTextNode(separator));
    }
  });
}

function setLanguage(language) {
  const normalizedLanguage = normalizeLanguage(language);
  const dictionary = translations[normalizedLanguage] || translations.en;
  document.documentElement.lang = normalizedLanguage;
  document.documentElement.dir = normalizedLanguage === "ar" ? "rtl" : "ltr";
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (dictionary[key]) {
      renderLocalizedText(node, dictionary[key], normalizedLanguage);
    }
  });
  localStorage.setItem("language", normalizedLanguage);
  languageSelect.value = normalizedLanguage;
  const selectedOption = [...languageSelect.options].find((option) => option.value === normalizedLanguage);
  if (languageLabel && selectedOption) {
    languageLabel.textContent = selectedOption.textContent;
  }
  scheduleSmartTextFit();
}

function checkoutKeyForLanguage(language) {
  const normalizedLanguage = normalizeLanguage(language);
  if (normalizedLanguage === "zh-Hant") {
    return "hkd_zh_hant";
  }
  if (normalizedLanguage === "zh-Hans") {
    return "cny_zh_hans";
  }
  if (normalizedLanguage === "ja") {
    return "jpy_ja";
  }
  return "usd_en";
}

function stripeLocaleForLanguage(language) {
  return (
    {
      en: "en",
      ko: "ko",
      vi: "vi",
      ar: "ar",
      fr: "fr",
      es: "es",
      de: "de",
      ru: "ru"
    }[normalizeLanguage(language)] || "en"
  );
}

function withStripeLocale(url, locale) {
  try {
    const checkoutUrl = new URL(url);
    checkoutUrl.searchParams.set("locale", locale);
    return checkoutUrl.toString();
  } catch {
    return url;
  }
}

function checkoutHrefForKey(key, language, explicitKey = false) {
  const links = window.LAZYING_STRIPE_PAYMENT_LINKS || {};
  const url = links[key]?.url || window.LAZYING_STRIPE_PAYMENT_LINK || "";
  if (typeof url !== "string" || !url.startsWith("https://")) {
    return fallbackOrderHref;
  }
  if (!explicitKey && key === "usd_en") {
    return withStripeLocale(url, stripeLocaleForLanguage(language));
  }
  return url;
}

function applyCheckoutLink() {
  const language = normalizeLanguage(languageSelect.value);
  const dictionary = translations[language] || translations.en;

  document.querySelectorAll("[data-checkout-link]").forEach((link) => {
    const explicitKey = Boolean(link.dataset.checkoutKey);
    const key = link.dataset.checkoutKey || checkoutKeyForLanguage(language);
    const href = checkoutHrefForKey(key, language, explicitKey);
    link.href = href;
    if (href === fallbackOrderHref) {
      link.removeAttribute("target");
      link.removeAttribute("rel");
      const fallbackKey = link.getAttribute("data-i18n-fallback");
      if (fallbackKey && dictionary[fallbackKey]) {
        link.textContent = dictionary[fallbackKey];
      }
    } else {
      link.target = "_blank";
      link.rel = "noopener";
    }
  });
}

function applyPriceLabels() {
  const links = window.LAZYING_STRIPE_PAYMENT_LINKS || {};
  document.querySelectorAll("[data-price-value]").forEach((node) => {
    const value = links[node.dataset.priceValue]?.discount;
    if (value) {
      node.textContent = value;
    }
  });
  document.querySelectorAll("[data-price-original]").forEach((node) => {
    const value = links[node.dataset.priceOriginal]?.original;
    if (value) {
      node.textContent = value;
    }
  });
}

function fitSmartText() {
  document.querySelectorAll(".smart-text").forEach((node) => {
    node.style.fontSize = "";

    const chunks = [...node.querySelectorAll(".text-chunk")];
    if (!chunks.length) {
      return;
    }

    const availableWidth = node.clientWidth;
    const widestChunk = Math.max(...chunks.map((chunk) => chunk.scrollWidth));
    if (!availableWidth || widestChunk <= availableWidth) {
      return;
    }

    const currentSize = Number.parseFloat(getComputedStyle(node).fontSize);
    const scale = Math.max(0.56, (availableWidth - 2) / widestChunk);
    node.style.fontSize = `${Math.floor(currentSize * scale)}px`;
  });
}

function scheduleSmartTextFit() {
  window.cancelAnimationFrame(smartTextFrame);
  smartTextFrame = window.requestAnimationFrame(fitSmartText);
}

function updateActiveCategory() {
  const activeHref = window.location.hash === "#panda" ? "#panda" : "#top";
  document.querySelectorAll(".category-link").forEach((link) => {
    link.classList.toggle("is-active", link.getAttribute("href") === activeHref);
  });
}

function initProductCarousel() {
  document.querySelectorAll("[data-product-carousel]").forEach((carousel) => {
    const slides = [...carousel.querySelectorAll(".carousel-slide")];
    const dots = [...carousel.querySelectorAll("[data-carousel-dot]")];
    const prev = carousel.querySelector("[data-carousel-prev]");
    const next = carousel.querySelector("[data-carousel-next]");
    let activeIndex = 0;
    let touchStartX = 0;

    function showSlide(index) {
      activeIndex = (index + slides.length) % slides.length;
      slides.forEach((slide, slideIndex) => {
        const isActive = slideIndex === activeIndex;
        slide.classList.toggle("is-active", isActive);
        slide.setAttribute("aria-hidden", String(!isActive));
      });
      dots.forEach((dot, dotIndex) => {
        const isActive = dotIndex === activeIndex;
        dot.classList.toggle("is-active", isActive);
        dot.setAttribute("aria-selected", String(isActive));
      });
    }

    prev?.addEventListener("click", () => showSlide(activeIndex - 1));
    next?.addEventListener("click", () => showSlide(activeIndex + 1));
    dots.forEach((dot) => {
      dot.addEventListener("click", () => showSlide(Number(dot.dataset.carouselDot)));
    });
    carousel.addEventListener("keydown", (event) => {
      if (event.key === "ArrowLeft") {
        showSlide(activeIndex - 1);
      }
      if (event.key === "ArrowRight") {
        showSlide(activeIndex + 1);
      }
    });
    carousel.addEventListener(
      "touchstart",
      (event) => {
        touchStartX = event.touches[0]?.clientX || 0;
      },
      { passive: true }
    );
    carousel.addEventListener(
      "touchend",
      (event) => {
        const touchEndX = event.changedTouches[0]?.clientX || 0;
        const delta = touchEndX - touchStartX;
        if (Math.abs(delta) > 42) {
          showSlide(activeIndex + (delta < 0 ? 1 : -1));
        }
      },
      { passive: true }
    );

    carousel.tabIndex = 0;
    showSlide(0);
  });
}

